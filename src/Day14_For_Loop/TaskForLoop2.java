package Day14_For_Loop;

public class TaskForLoop2 {
    public static void main(String[] args) {

        // write a java program which calculates the sum of numbers [0 to 5];
        // using for-loop
        // 0 + 1 + 2 + 3 + 4 + 5

        int sum = 0;

        for (int i =0; i <= 5; i++) {
            sum =sum + i;

        }
        System.out.print(sum);


    }
}
