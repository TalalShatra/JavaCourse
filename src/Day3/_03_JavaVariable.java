package Day3;

public class _03_JavaVariable {

    public static void main(String[] args) {

        // you can create variables in the same line but not recommended


        int year = 2022, month = 4;

        System.out.println(year);
        System.out.println(month);

        // updating variable's value
        month = 5;
        System.out.println(month);


    }
}
