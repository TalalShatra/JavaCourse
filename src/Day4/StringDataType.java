package Day4;

public class StringDataType {
    public static void main(String[] args) {

        System.out.println("TechnoStudy");

        String schoolName = "Techno Study";
        System.out.println(schoolName);
        // no Limit for characters

        String name = "Talal";
        String lastName = "Shatra";

        System.out.println(name);
        System.out.println(lastName);

        System.out.println(name + lastName);
        System.out.println(name + " " + lastName);
        System.out.println(name + "\t" + lastName);

        String question = "How old are you";
        String answer = "I,m ";
        int age = 25;

        System.out.println(question +"\n" + answer + age);
        /* If at least 1 String exist inside parenthesis
        everything else will be converted to String.
         */




    }
}
